package com.city.bbs.module.model;

public class ModuleModel {
	private int id;
	private String module_name;
	private String module_cont;
	private String user_name;
	private String cs_pic;
	private String cs_color;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getModule_name() {
		return module_name;
	}
	public void setModule_name(String module_name) {
		this.module_name = module_name;
	}
	public String getModule_cont() {
		return module_cont;
	}
	public void setModule_cont(String module_cont) {
		this.module_cont = module_cont;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}
	public String getCs_pic() {
		return cs_pic;
	}
	public void setCs_pic(String cs_pic) {
		this.cs_pic = cs_pic;
	}
	public String getCs_color() {
		return cs_color;
	}
	public void setCs_color(String cs_color) {
		this.cs_color = cs_color;
	}
	
	
}
